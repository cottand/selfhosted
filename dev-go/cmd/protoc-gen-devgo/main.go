package main

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
)

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		for _, file := range gen.Files {
			if !file.Generate {
				continue
			}
			generateFile(gen, file)
		}
		return nil
	})
}

const (
	contextPackage = protogen.GoImportPath("context")
	grpcPackage    = protogen.GoImportPath("google.golang.org/grpc")
	syncPackage    = protogen.GoImportPath("sync")
	codesPackage   = protogen.GoImportPath("google.golang.org/grpc/codes")
	statusPackage  = protogen.GoImportPath("google.golang.org/grpc/status")

	bedrockPackage = protogen.GoImportPath("github.com/cottand/selfhosted/dev-go/lib/bedrock")
)

func generateFile(gen *protogen.Plugin, file *protogen.File) error {
	filename := file.GeneratedFilenamePrefix + "_devgo.pb.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)

	g.P("// Code generated by protocplugin. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()
	if err := loadClientFunc(g, file.Services[0]); err != nil {
		return err
	}

	for _, service := range file.Services {
		for _, method := range service.Methods {
			if err := clientCall(g, method); err != nil {
				return err
			}
		}
	}

	return nil
}

